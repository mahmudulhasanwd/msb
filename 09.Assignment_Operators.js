// Assignment Operators (=, +=, -=, *=, /=, **=,%=, ++, --)

var num1 = 10;
var num2 = 10;
num2 = num1
console.log(num1);
console.log(num2);

num1 +=5;
console.log(num1);

num1 -=5;
console.log(num1);

num1 *=3;
console.log(num1);

num1 **=2;
console.log(num1);

num1 /=2;
console.log(num1);

num1 %=2;
console.log(num1);

num1 ++;
console.log(num1);

num1 --;
console.log(num1);

