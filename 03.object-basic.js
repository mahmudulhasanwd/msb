let user = {
   name : 'john',
   age : 20,
   isStudent : true
}
console.log(user);

// get one property form object with dot notaion
console.log(user.name);
console.log(user.age);
console.log(user.isStudent);

// get one property form object with bracket notation
console.log(user['name']);
console.log(user['age']);
console.log(user['isStudent']);

